#include <iostream>
using std::cout;
using std::endl;

int main()
{
	cout << "Here is a poem that I like:" << endl;
	cout << "" << endl;
	cout << "Had I the heaven's embroidered cloths," << endl;
	cout << "Enwrought with golden and silver light," << endl;
	cout << "The blue and the dim and the dark cloths" << endl;
	cout << "Of night and light and the half-light;" << endl;
	cout << "I would spread the cloths under your feet:" << endl;
	cout << "But I, being poor, have only my dreams;" << endl;
	cout << "I have spread my dreams under your feet;" << endl;
	cout << "Tread softly because you tread on my dreams." << endl;
	return 0;
}